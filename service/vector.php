<section class="diagnosis-section">
    <h2 class="section-title">Vector Control</h2>
    
    <h3 style="color: #8B4513; margin-top: 30px; text-align: center; margin-bottom: 20px;">The longest combat against the most dreadful enemy of the history of Sri Lanka</h3>
    
    <h4 style="color: #8B4513; margin-top: 20px; margin-bottom: 15px;">The battle against malaria vector</h4>
    
    <h4 style="color: #8B4513; margin-top: 20px; margin-bottom: 15px;">Disclosure of the unknown enemy</h4>
    
    <div class="info-list">
        <p>"The mysterious fever" battered the civilization of the Island nation for centuries was still a "wild fever" at the beginning of last century, for which nobody knew the reason behind, until the colonial masters had the wisdom of revealing it. Mannar was the historical site which broke through the transmitter of malaria in the past century, and a century later, it was the same site from where another major discovery done in 2016 with regarding malaria</p>
        
        <p>In 1913 Lt Colonel S.p.James and Dr S T. Gunasekara divulged A. culicifasis as the vector transmitting malaria in Thalaimannar and incriminated as the vector for malaia. Further from 1921 to 1926 it was unearthed the</p>
        <ul style="margin-left: 30px;">
            <li>Distribution of endemic malaria</li>
            <li>Seasonality incidence of malaria</li>
            <li>Presence of different parasites in the country</li>
        </ul>
    </div>
    
    <h4 style="color: #8B4513; margin-top: 20px; margin-bottom: 15px;">The launch of the battle: Initial control measures; embryonic but environmental friendly</h4>
    
    <div class="info-list">
        <p>Organized control activities were commenced with the establishment of Anti Malaria Campaign (AMC) center at Kurunegala town in 1911. Later, AMC centers were established in Mahara prison, Anuradhapura (1922), Trincomalee (1923), Kurunegala and Chilaw. "Carters report" published in 1927 states that anti mosquito measures done at Mahara prison, Anuradhapura and Trincomalee were confined to filling, draining, oiling, and jungle clearing. In Anuradhapura, control measure was began in experimental basis to target anopheline breeding sites. In 1925, a reconstruction of Halpan ela, a stream running through Anuraghepura town was done targeting malaria control.</p>
        
        <p>The measure taken to control malaria in those ere were mainly targeted to</p>
        <ul style="margin-left: 30px;">
            <li>Elimination or reduction of malaria carrying mosquitos</li>
            <li>Destruction of malaria parasites in infected persons</li>
            <li>Protection of man biting from mosquitos</li>
            <li>Health education of public</li>
        </ul>
        
        <p>Larvivorous fish was used for larval control in wells and temporary rain pools from the beginning of the last century. Fish were introduced to Anuradhapura from Colombo. Initially Haplochilus lineatus and Lebistes reticulatus were introduced, but the former was failed as it was not breeding in natural breeding sites in Anuradhapura. A nursery was constructed in Anuradhapura to breed fish. As the presence of Lula, "Ophiocephalus striatus" was identified as a threat to survival of introduced fish, wells were cleaned and the water were pumped out to assure no "Lula" was in the breeding sites before applying L. reticularis. Man-made fish breeding sites were later built in Trincomalee and Jaffna as well. Continuous reapplication of fish had to be maintained as there were some issues which killed all the fish time to time. Such was happened when wells were disinfected during Cholera outbreak in 1925.</p>
        
        <p>In mid nineteen twenties, it was apprehended that a more organized control programme was needed for a better control. The acting Principal Civil Medical Officer recommended that</p>
        <p style="font-style: italic; margin-left: 30px;">"For next 10 years, anti mosquito measure only by central malaria organization for which local authorities should assist in terms of maintaining."</p>
        
        <p>In urban areas, where less irrigation channels and less cultivation, cost effective method to adopt was larval control by using "Iol " and "Paris green" and environmental measures. Sometimes adult spray killing of mosquitos were used as "supplementary measures". In the rural areas which were large areas in territory, anti larval methods were not cost effective.</p>
        
    </div>
    
    <h4 style="color: #8B4513; margin-top: 20px; margin-bottom: 15px;">Malaria epidemic: 1934/1935</h4>
    
    <div class="info-list">
        <p>The long drought and failing of north east monsoons in 1934 and 1935 made the stage for a devastating malaria epidemic which is said to be the worst ever malaria epidemic in known history of Sri Lanka. The epidemic erupt in Alauwa, on the left bank on Maha oya spread to 5 major basins of rives namely Maha oya, Deduru oya, Kelani Ganga Kalu ganga and Mahaweli ganga. It covered the entire wet zone but amazingly spare the dry zone.</p>
        
        <p>During 1939/40 epidemic adult spray killing using a mixture contain pyrethrum was carried out in limited extent. Even though it was revealed that this method was effective, due to inability to maintain stocks of chemicals, spraying was done in limited scale and in selected areas only. Instead focus was on motivating people to do spraying by themselves and the government provided the mix of chemicals and the equipment free of charge or at a nominal fee. In addition, oiling of rivers and streams were done by using Shell Malariol. Paris green, which was superior to oil, was used as the anti-larval method, as the former could be used for the paddy fields and wells, of which water are used for dining purposes. In 1938 government initiated to build automatic flushing devises, which were meant to flush out malaria vector larvae and it was identified as an effective strategy to control malaria.</p>
    </div>
    
    <h4 style="color: #8B4513; margin-top: 20px; margin-bottom: 15px;">The DDT era</h4>
    
    <div class="info-list">
        <p>Adulticiding using spraying chemicals was outplayed by short duration of action of chemicals which necessitate frequent re application. Dichloro Diphenyl Trichloroethane (DDT) was found to be effective in controlling malaria adult vectors. In 1939 and in 1945, during the malaria epidemic, health department obtained a small stock of DDT from Army services. With the observed success, the use of DDT was up scaled and it helped to interrupt the malaria transmission soon. At the beginning, 5% of technical DDT, diluted in kerosene was used in Anuradhapura and in Kekirawa by engaging two mobile units. The interval between two spraying was 4 weeks which was increased to 6 weeks in shortly. Later, suspension of 5% DDT in 40% of emulsion concentrate of Zylene was used. Later, it was also replaced by spraying DDT suspension of 50% DDT water suspension powder and water was used as the diluent. The interval varied between 6 to 10 weeks. By 1947, mass scale Indoor Residual spraying (IRS)programme was launched throughout the island where the spleen rate was more than 10%. In 1949 Banzine Hexachloride was also started to use.</p>
        
        <p>Spraying units were formed for mass spraying operations. Basically, three types of spraying units were in operation:</p>
        <ol style="margin-left: 30px;">
            <li>A truck unit- Sanitary supervisor was the in charge with 2 to 3 supervisors and 12 to 15 laborours</li>
            <li>A jeep unit_ Sanitary inspector was the supervisor and team includes a driver-supervisor and less than 6 labourers</li>
            <li>A walking unit- Supervised by an overseer and having variable number of laborious</li>
        </ol>
        
        <p>Immediate supervision of the programme was done by Medical Officer of Health or the Divisional superintendent of Health.</p>
        
        <p>The average number of houses to be sprayed was 20 per a day. The roof, the eves, inner surface of walls and under surfaces of furniture were sprayed and each sprayed house was given a house hold card.</p>
        
        <p>Latter, spraying units were replaced by Vigilance units in endemic and epidemic areas. This unit was tasked with monitoring the potency of the insecticides in addition to the spraying process. In 1952, After 5 years of introduction of country wide DDT, a tenfold reduction of cases were observed. During this time supervision of vector control activities by PHI was established and supervision of spraying teams were done by vigilance units.</p>
        
        <p>By 1951, malaria transmission had interrupted in both wet and intermediate zones. This remarkable success and the evidence of development of resistance to DDT in other countries, paved the way to gradually discontinuation of truck units and jeep units in phase wise. Going in par with the decisions made at the Malaria conference in Philipines, use of DDT was restricted to areas where transmission is evident. In 1955, spraying was abandoned in entire epidemic areas, in urban areas and in settled villages in endemic areas. In parallel, 15 emergency jeep unis were established in 7 provinces to carry out spraying activities in indicated areas.</p>
        
        <p>With this, the spraying turned out to be a focal spraying, which was in par with focal case base management.</p>
    </div>
    
    <h4 style="color: #8B4513; margin-top: 20px; margin-bottom: 15px;">Malaria eradication attempt: 1958-1963</h4>
    
    <div class="info-list">
        <p>With the introduction of IRS using DDT, major colonization schemes were slowly getting rid of malaria. The dry zone was gradually getting repopulated. But the decision of scale downing of IRS by using DDT back fired as malaria gradually started to remerge, which warrants the health authorities to recommend reinstallation of IRS and continuous surveillance of active foci. In addition, reported resistance to DDT in other countries prompted the health authorities to embark on eradication programme soon. A 5 year malaria eradication programme was launched in December 2nd of 1958 which had three phases:</p>
        <ol style="margin-left: 30px;">
            <li>Preparatory phase - Preparatory ground work as geographical reconnaissance, procurement of insecticides training were to be done in this phase</li>
            <li>Attack phase - Houses of Entire dry zone was sprayed with DDT in a dose of 50 mg/sq feet, so that a repeated spraying in 6 month would adequate as the residual action persisted for 6 months. As wet and intermediate zones were seemed to already interrupted the transmission, those areas were not included in the attack phase. Thus, directly implemented the actions earmarked in consolidation phase</li>
            <li>Consolidation Phase - Attention was given to passive and active case detection. Whenever, a case was reported residual spraying was done in area of Â½ to 1 mile radius</li>
        </ol>
        
        <p>As a result of the programme in 1962 only two foci with active transmission were detected. In 1963 partial withdrawal of IRS was done and no foci of active transmission was detected in the said year. In April 1964 spraying was completely withdrawn and the entire country was managed under consolidation phase.</p>
    </div>
    
    <h4 style="color: #8B4513; margin-top: 20px; margin-bottom: 15px;">Resurgence of Malaria</h4>
    
    <div class="info-list">
        <p>The state of interrupted transmission was not lasted long. With the total withdrawal of residual spraying in latter part of 1964, active foci of P. falciparum and P. malaria transmission was detected. Even though P.malaria foci were controlled, the P. falciparum foci in the eastern parts of the country was remaining warranting re introduction of residual spraying reverting back to attack phase.</p>
        
        <p>As it was detected that 10% of the reported cases were imported cases mostly form Maldives, anti-mosquito measure were taken in ports of Entries.</p>
        
        <p>With the continuous transmission, perennial spraying by using DDT was done in the districts of Trincomalee, Ampara, Polonnnaruwa, Anuradhapura and Kurunegale.</p>
        
        <p>In 1966 and 1967, number of cases detected was alertly high and mostly in areas of new development projects and chena cultivation areas. In 1967 out breaks were detected in Elahera area where gem mining was extensively done which attracted people from all over the country. Perennial Residual spraying was continued and in other areas focal spraying was done when ever cased are reported and in areas where developmental projects were in operation. But the epidemic which was only second to the mass epidemic in 1934/35 and was involving entire dry zone and large part of intermediate zone. Localized out breaks were evident in wet zones.</p>
        
        <p>In 1969, possible resistance for DDT was detected by Anti Malaria Campaign and it is believed that the resistance to DDT contributed in a big way for the extensive island wide epidemic of malaria. By 1974 DDT resistance was evident in all malaria vector species. In addition to the insecticide resistance gradual public resistance was also building against spraying of DDT which also a contributing factor for failing mosquito control. With those it was decided to do the residual spraying under strict supervision only in highly indicated instances.</p>
        
        <p>After a situational analysis done in 1976 it was recommended that:</p>
        <ul style="margin-left: 30px;">
            <li>Vector resistance for DDT is well established</li>
            <li>Main control strategy should be IRS with Malathion and the transmission should be interrupted as soon as possible to avert development of resistance to malathion</li>
            <li>To ban the use of Malathion and related insecticides such as fenitrothion for other purposes other than for malaria control</li>
        </ul>
        
        <p>Studies done in many areas across the country revealed that the malaria vectors are susceptible for Malathion. AMC revert to IRS using malathion which needed to spray once in every 3 months. In addition being an Organo-phospate (OP), stringent safety methods were to be taken and estimation of chlorine esterase levels were to be done in every 3 months.</p>
        
        <p>In 1975 IRS using malarion was commensed in pase based manner in areas highly battered with P.falciparum in Uva province. In 1976 it was ween the end of usage of DDT of spraying after 30 years. With this DDT was replaced with malathion throughout the country. As a measure to delay the development of resistance government decided to ban the importation and usage of malathion on agricultural purposes and further to ban the usage of Fenotrothion which was earmarked as the alternative in case of malathion resistance, for the agriculture. The decision was given the legal status by an act of parliament.</p>
    </div>
    
    <h4 style="color: #8B4513; margin-top: 20px; margin-bottom: 15px;">Intense malaria programme 1977 to 1982</h4>
    
    <div class="info-list">
        <p>In 1973 to 1975, 8% of total health budget was to control malaria. Realizing this and the failure to control the malaria epidemic, a 5 year malaria control programme was launched costing Rs 86 million, 75% of which was for the procurement of insecticides.</p>
        
        <p>Under the programme, population of 4,276,100 were supposed to cover with spraying malathion and all the inbuilt structures were targeted to be sprayed. In addition supplementary methods were identified to control the vector. Temephose 50% EC was used as chemical larviciding. Initially the Manik river basin was targeted. Flushing of the water in Polgolla saw reduction of malaria cases drastically in an area of 9 Km beyond but due to scarcity of water, this strategy was not materialized in the dry season.</p>
        
        <p>Susceptibility testing done in 1980, after 5 years of using of malathion revealed a satisfactory susceptible status. The incidence of malaria was dropped by 84.4% at the end of 5 years but AMC has decided to take maximum possible steps to delay the development of resistance. Accordingly it was planned to carry out spraying in more evidence based manner by stratifying the areas according to the risk level as follows:</p>
        
        <div class="risk-level-table">
            <table>
                <tr>
                    <td>Jaffna peninsula</td>
                    <td>Once a year preceding transmission season</td>
                </tr>
                <tr>
                    <td>Northern dry belt</td>
                    <td>Twice a year preceding transmission season</td>
                </tr>
                <tr>
                    <td>Eastern foot hills</td>
                    <td>Four times a year preceding transmission season</td>
                </tr>
            </table>
        </div>
        
        <p>In 1982 susceptibility test suggested of possible resistance in certain area for 5% malathion. BIO assays tests revealed that residual spraying was not done in proper way.</p>
        
        <p>In mid 90's WHO recommended to use insecticide treated bed nets for personnel protection and to interrupt the transmission of malaria. 1986 introduction of insecticide treated bed nets was carried out.</p>
        
        <p>AMC promptly promote the use of insecticide treated bed nets and those were given free of charges. Permethrin Emulsion concentrates were used for immersion of nets. A sharp reduction of malaria cases was evident in 1988, even without upscaling of spraying.</p>
        
        <p>In 1993 low levels of susceptibility for malathion was evident in Western, North central, North Western and Sabaragamuwa provinces.</p>
        
        <p>Due to emerging resistance levels in 1993, malathion was replaced by Lamda cyhalothrine in Kurunegala and in 1994 Fenitrithion was introduced to Puththalm. In par with this, WHO also had advised on rotational use of insecticides.</p>
    </div>
    
    <h4 style="color: #8B4513; margin-top: 20px; margin-bottom: 15px;">2008-2012 Action Plan</h4>
    
    <div class="info-list">
        <p>IIRS coverage in foci and distribution of Long lasting insecticide impregnated bed nets (LLIN/ITN) and other vector control methods as complementary measures when indicated</p>
        
        <p>GFATM started to aid AMC in 2003 and funded for procuring LLINS for the first time in Sri Lanka amounting to 300,000; 500,000; in 2002 and 2004 respectively</p>
    </div>
    
