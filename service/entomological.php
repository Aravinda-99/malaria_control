        <h3 class="subsection-title">Malaria Vectors</h3>
        <p>Anopheles culicifacies is the major vector in Sri Lanka which occurs as a species complex (A,B,C, D, and E) and species E being incriminated as the primary vector. An. culicifacies remains prevalent in the country as during the control period. The Anopheles subpictus has been incriminated as secondary vector and there are potential vectors Anopheles annularis, Anopheles vagus and Anopheles varuna.</p>
        
