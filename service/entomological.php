    <h2 class="section-title">Entomological Surveillance</h2>
    
    <div class="full-width-image">
        <img src="asset/image/Ento_Pic6.jpg" alt="Mosquito specimens">
    </div>
    
    <div class="content-container">
        <p>Entomological surveillance is a major component of the malaria prevention of re-introduction strategy of the country to measure the levels of receptivity. Anti Malaria Campaign (AMC) implements a comprehensive package of entomological surveillance activities among 22 RMO regions throughout the country through 43 entomological teams. Entomology is a key discipline that has helped to describe epidemiological trends and transmission patterns of malaria in Sri Lanka during the control phase. In elimination and post elimination era entomological surveillance played a vital role in monitoring the vector densities throughout the country despite the setting of malaria free status in the country.</p>

        <h3 class="subsection-title">Control Phase</h3>
        <p>An entomological surveillance system on Malaria was established in Sri Lanka shortly after the 1934â€“1935 epidemic, aiming to forecast increases in seasonal transmission and potential epidemics through identification of changes in vector breeding. Trained mosquito collectors collected larvae and adult mosquitoes in dwellings on a monthly schedule. In 1940s the programme added mandatory inspections of rivers and streams for larvae by public health inspectors in each jurisdiction. Thereafter, entomological surveillance was carried out as per the dictates of a control programme focusing on many aspects using different techniques. In 1945, an extensive indoor residual spraying (IRS) operation was launched. The early success of IRS in reducing the malaria burden constrained the country to embark on a malaria eradication strategy in 1957, coinciding with the WHO's Global Malaria Eradication Programme. In 1963, near eradication was achieved with only 17 cases and 11 of this number were imported cases.</p>
        
        <p>This success created a severe cutback in political and financial support for malaria control, leading to the withdrawal of malaria control measures such as IRS and a weakening of surveillance systems. In the period where an extensive malaria control programme was launched in 1977-1982, several entomology teams were to function, each with an Entomological Assistant, 2 Field Assistants, and 5 Labourers. Each team was provided with a vehicle and the driver. Routine entomological investigations would include insecticide susceptibility studies, bio-assay tests, man-biting rates, parous rates, indoor resting densities and exit window trap observations, among others. Studies related to chemical larviciding, and the use of larvivorous fish, were also carried out with view to expand the use of supplementary methods of vector control (AMC sources).</p>
        
        <p>Decentralization of health services in March 1989 was a very important milestone in the history of health services in Sri Lanka, in general, and in particular, for the Anti Malaria Campaign which until then functioned as a strictly vertical campaign. Thus the Regional Medical/ Malaria Officers were placed under the purview of the provincial authorities, administratively, but remained under the technical guidance of the Director of the Anti Malaria Campaign and continued in their regions. GFATM Round 1 Project on Intensive malaria control among the hitherto marginalized populations in conflict-affected districts in Sri Lanka was commenced from 2003 and the Districts covered through the project were Jaffna, Kilinochchi, Mannar, Mulativu, Vavuniya, Trincomalie, Batticaloa, Ampara, Kalmune, Polonnaruwa and Anuradhapura. This project considerably helped the Anti mMalaria Campaign to control malaria in the stipulated districts.</p>
        
        <p>GFATM Round 4 covered the Districts Puttalam, Kurunegala, Matale, Ratnapura, Hambantota &Moneragala and strengthening of the epidemic forecasting system by augmentation of entomological surveillance was one of the main objectives of this project.</p>
        
        <h3 class="subsection-title">Pre-elimination phase</h3>
        <p>During the pre-elimination phase, commenced from September 2009 both parasitological and entomological surveillance was intensified with funding provided by the GFATM. At the end of the separatist conflict in the north and east of the country in May 2009, the capacity of the AMC was insufficient to intensify surveillance in these areas and a private sector organization, TEDHA, was requested to augment such services in the Eastern Province and in selected districts in the Northern Province (Mannar) including entomological surveillance. Entomological surveillance was carried out by both the AMC and TEDHA using similar formats and techniques. The techniques used have been described in previous sections. Entomological surveillance was assessed as the number of days entomological activities were carried out in the field. While GFATM funded all entomological surveillance activities of TEDHA, entomological activities carried out by the AMC HQ and Regional Offices were funded by the GFATM and Government of Sri Lanka through its provincial budgets.</p>
        
        <h3 class="subsection-title">Elimination Phase</h3>
        <p>There was a significant decrease in the number of indigenous malaria cases from 2010 to 2011 and entomological monitoring continued as sentinel monitoring and spot checks. In this period thirty three entomological teams engaged in entomological monitoring at central and district levels by the MoH. These thirty three teams were funded with additional 14 entomological days through GFATM round 8 funding. TEDHA has continued their activities during this period in the assigned areas mentioned above.</p>
        
        <h3 class="subsection-title">Prevention of Reintroduction (PoR) Phase (2013 to date)</h3>
        <p>Thirty three entomological teams were engaged in entomological monitoring at AMC programme in 2013-2014. The TEDHA entomology activities continued until September 2014 in the respective districts. In 2015, it was increased up to 38 entomological teams in the latter part of the year according to the proposed 45 teams in the National Strategic Plan and now there are 43 entomological teams carrying out malaria entomological surveillance activities in the country.</p>
        
        <h3 class="subsection-title">Re-orientation of the Entomological surveillance system</h3>
        <p>The entomological surveillance activities have changed very little from the control through elimination and prevention of reintroduction phases even after the country has been certified a malaria free country by WHO and a requirement re-organizing the existing system. Therefore, an optimal, rather than exhaustive set of entomological surveillance operations, a reviewed new system has been introduced together with the local and international expert views.</p>
        
        <h3 class="subsection-title">The scope and scale of entomological surveillance</h3>
        <p>Entomological surveillance is an essential pre-requisite for judicious malaria vector control as the findings provide critical information on vector species, their distribution, density, bionomics and susceptibility/ resistance to insecticides used for malaria control. In addition, these investigations are useful for the monitoring of potential vectors and the role they could play in disease transmission. Information routinely collected through entomological surveillance techniques assist in identifying spatial and temporal trends and understanding the changes in receptivity, the need and the timing for intervention as well as efficacy and effectiveness of malaria vector control measures employed. Extensive short-term proactive spot checks assist in identifying the occurrence of vector species and assessment of receptivity. Reactive spot surveys have to be carried out in all areas with reported cases as per the scope of work to be performed when a malaria patient is reported.</p>
        
        <h4>Types of entomological surveys</h4>
        <ol>
            <li>Proactive spot checks</li>
            <li>Reactive spot checks</li>
            <li>Routine sentinel surveys</li>
            <li>Extended routine sentinel surveys</li>
        </ol>
        
        <h4>The entomological surveillance techniques used in entomological surveys</h4>
        <ol>
            <li>Larval Survey</li>
            <li>Cattle-baited Huts (CBH)</li>
            <li>Indoor hand collections/Spray sheet collections</li>
            <li>Cattle baited net trap collections</li>
            <li>Outdoor resting collection and collections/spray sheet collections</li>
            <li>Human landing collection (HLC) partial and full</li>
            <li>Ovary dissections for parity</li>
            <li>Insecticide susceptibility testing (annual basis for each class of insecticides)</li>
            <li>Bio-efficacy testing for vector control interventions</li>
        </ol>
