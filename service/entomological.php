        <h3 class="subsection-title">Recent finding of Anopheles Stephensi from Northern Province</h3>
        <p>Container breeding malaria vector in urban settings of neighboring countries: The newest challenge in malaria vector surveillance and response. Anopheles stephensi was found for the first time in Sri Lanka by a larval survey done in a spot check done at Pesalai area of Mannar MOH. This species Breeds predominantly in wells and in Cement tanks and water storage utensils such as barrels. In 2017, it is found from all the districts (Jaffna, Kilinochchi, Mallaitivu, Mannar and Vavuniya) of the Northern Province.</p>
        
