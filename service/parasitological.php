    <h2 class="section-title">Parasitological Surveillance</h2>
    <div class="surveillance-content">
        <p>The Parasitological Surveillance in the country includes screening of individuals attending medical institutions and village level screening done depending the vulnerability and receptivity risk. Screening done at medical institutions is categorized as Passive Case Detection (PCD) which included medical institutions where is no PHLT or PHFO or Activated Passive Case Detection (APCD) which includes medical institution where there is either a PHLT and/or a PHFO. Village level screening is done by Active Case Detection (ACD) and Mobile malaria clinics. Microscopy is the main diagnostic method while Rapid Diagnostic tests (RDTs) are also being used as a supplementary tool.</p>
        
        <p>Activated Passive Case Detection (APCD) is the most important method of detection of malaria cases, accounting for about 97% of the cases detected. The Anti Malaria Campaign recommends screening all suspected fever patients with a travel history to malarious country attending activated medical institutions (APCD centres) for malaria. At present approximately 182 medical institutions, located predominantly in the dry and intermediate zones of the country have been activated through the appointment of a PHLT and/or a PHFO. Almost all the important medical institutions in the previously malaria endemic areas APCD centres. About 25% of the total blood smears screened in the country falls under this category.</p>
        
        <p>Active Case Detection (ACD) and Mobile malaria clinics are carried out in high risk areas and among vulnerable populations. These methods are mainly used to facilitate early detection of malaria cases (including asymptomatic parasite carriers) thereby reducing the possibility of transmission. About 35% of the total blood smears screened in the country falls under this category.</p>
        
        <p>Screening potential blood donors and donor blood for malaria is another important function done by the PHLTT attached to AMC. This accounts for approximately 40% of total blood films screened. Screening of all donor blood for malaria before transfusion is mandatory in Sri Lanka at present.</p>
        
