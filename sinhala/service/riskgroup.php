        <h3 class="riskgroup-subtitle">Public Awareness in The Phase of Prevention of Reintroduction of Malaria</h3>
        
        <div class="riskgroup-content">
            <p>Sri Lanka reported no malaria cases after October 2012. Now already passed five years with no indigenous malaria. But so many imported malaria cases are getting reported. As Sri Lanka is having many high vulnerability and high receptivity areas there is a likelihood to have an introduced malaria case. To avoid this happen public awareness is very much needed. Already there are identified many risk groups in Sri Lanka. Therefore public awareness should be enhanced after the malaria free certification of the country.</p>
            
            <p>Public awareness is the public's level of understanding about the importance and implications of a certain programme or an activity. Raising public awareness is not the same as telling the public what to do. It is explaining issues and disseminating knowledge to people so that they can make their own decisions. High public awareness occurs when a significant proportion of society agrees to certain decisions. The prevention of reintroduction of malaria is an issue that is of great importance to all citizens. When the public awareness is low a majority of people do not know or do not care about importation of a malaria cases.</p>

            <p>There are two main areas to focus on when raising awareness about prevention of importation of malaria. First, there is general public awareness, which involves widespread understanding of the issues on a societal level. Mass media programmes in television and radio channels in prime air time would be important in this case. Newspaper articles may also help. Second, there is self-awareness, which occurs when individuals understand the concept how a visit to a malarious country can affect them personally. There are different ways to raise public awareness about importation of malaria cases. It can be done through specific planned events in public places like hospitals, religious places, schools, port of entries and workplaces etc. using any available medium. Leaflets, brochures, poster campaigns, documentaries and websites, regarding signs and symptoms of malaria, availability of treatment and prophylaxis free of charge to the patients and travelers, free of charge blood testing after returning from a malarious country, and toll free hotline numbers are some of them. Message disseminating items like leaflets and brouchures should definitely be available at immigration desks of airports and seaports and in rooms of hotels with foreign tourists.</p>

            <p>Methodologically sound approaches to raising awareness, together with sufficient exposure have been shown, through social science research, to have an effect on knowledge, attitudes and behaviour. It is usually more effective to create a coordinated, long-term awareness-raising campaign than to create large, short-term campaign. This is because when the concept of prevention of reintroduction of malaria is talked about over time, its importance becomes normalized and it becomes a normal part of people's everyday lives. By contrast, if there is only one single campaign to address the issue, people may forget about the issues once the campaign is over. By taking different approaches at different times, awareness can be raised all over the country, using current events and issues as a backdrop for talking about prevent importation of malaria.</p>
